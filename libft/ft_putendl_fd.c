#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	long unsigned int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
		write(fd, &s[i++], 1);
	write(fd, "\n", 1);
}
